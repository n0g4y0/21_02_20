

public class Coordenada
{
    private int valorX;
    private int valorY;
    
    public Coordenada(int valorX, int valorY)
    {
        // initialise instance variables
        this.valorX = valorX;
        this.valorY = valorY;
    }
    
    public int getValorX(){
        return valorX;
    }
    
    public int getValorY(){
        return valorY;
    }

    
}
